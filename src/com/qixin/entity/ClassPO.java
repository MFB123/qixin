package com.qixin.entity;

public class ClassPO {
	private int class_id;
	private String name;
	private String start_time;
	private String end_time;
	private int mid;
	private String deleid;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	
	public int getClass_id() {
		return class_id;
	}
	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}
	
	public String getDeleid() {
		return deleid;
	}
	public void setDeleid(String deleid) {
		this.deleid = deleid;
	}
	@Override
	public String toString() {
		return "ClassPO [class_id=" + class_id + ", name=" + name + ", start_time=" + start_time + ", end_time="
				+ end_time + ", mid=" + mid + ", deleid=" + deleid + "]";
	}
	

	
	
}
